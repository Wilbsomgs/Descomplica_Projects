// Exemplo ilustrando o uso do operador NAO representado por (!)

const a = true; 
const b = false;

const resultado01 = !a;
const resultado02 = !b;

console.log(`esse operador inverte o valor ${resultado01} (a==true ficou false)`);
console.log(`esse operador inverte o valor ${resultado02} (b==false ficou true)`);